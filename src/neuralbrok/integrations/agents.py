# --- Public API ---

def setup(agent_slug: str, project_dir: Path, nb_url: str, api_key: str, global_dir: bool, dry_run: bool, force: bool) -> List[Path]:
    if agent_slug not in AGENT_REGISTRY:
        raise ValueError(f"Unknown agent: {agent_slug}")
    
    integration = AGENT_REGISTRY[agent_slug]
    if integration.setup_fn:
        return integration.setup_fn(project_dir, nb_url, api_key, global_dir, dry_run, force)
    return []

def list_agents() -> List[AgentIntegration]:
    return list(AGENT_REGISTRY.values())

def check_status(project_dir: Path) -> Dict[str, bool]:
    """Checks if each agent's primary config file exists."""
    status = {}
    for slug, integration in AGENT_REGISTRY.items():
        # Check primary path (first one)
        primary = integration.config_paths[0]
        if primary.startswith("~"):
            path = Path(primary).expanduser()
        else:
            path = project_dir / primary
        status[slug] = path.exists()
    return status

def remove_agent(agent_slug: str, project_dir: Path, global_dir: bool, dry_run: bool) -> List[Path]:
    if agent_slug not in AGENT_REGISTRY:
        raise ValueError(f"Unknown agent: {agent_slug}")
        
    integration = AGENT_REGISTRY[agent_slug]
    removed = []
    for rel_path in integration.config_paths:
        if rel_path.startswith("~"):
            path = Path(rel_path).expanduser()
        else:
            path = project_dir / rel_path
            
        if path.exists():
            if dry_run:
                click.echo(f"[DRY-RUN] Would remove {path}")
            else:
                path.unlink()
                removed.append(path)
    return removed

def get_installed_integrations(project_dir: Path) -> List[str]:
    """Returns a list of slugs for agents that appear to be installed."""
    status = check_status(project_dir)
    return [slug for slug, installed in status.items() if installed]
