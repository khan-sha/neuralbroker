def _setup_claude_code(project_dir: Path, nb_url: str, api_key: str, global_dir: bool, dry_run: bool, force: bool) -> List[Path]:
    base = Path.home() if global_dir else project_dir
    settings_path = base / ".claude" / "settings.json"
    claude_md_path = base / "CLAUDE.md"
    
    settings = {
        "api_endpoint": f"{nb_url}/v1",
        "api_key": api_key,
        "model": "claude-3-5-sonnet-20240620"
    }
    
    paths = []
    paths.append(_write_json(settings_path, settings, dry_run, force))
    
    md_content = f"NeuralBroker integrated. Run `neuralbrok code` to see routing status.\n"
    paths.append(_write_text(claude_md_path, md_content, dry_run, force))
    
    return paths

def _setup_cursor(project_dir: Path, nb_url: str, api_key: str, global_dir: bool, dry_run: bool, force: bool) -> List[Path]:
    # Cursor uses MCP or direct OpenAI compat
    base = Path.home() if global_dir else project_dir
    mcp_path = base / ".cursor" / "mcp.json"
    env_path = project_dir / ".env"
    
    mcp_config = {
        "mcpServers": {
            "neuralbroker": {
                "command": "neuralbrok",
                "args": ["mcp"],
                "env": {"NB_API_KEY": api_key}
            }
        }
    }
    
    paths = []
    paths.append(_write_json(mcp_path, mcp_config, dry_run, force))
    
    # Cursor also supports .env for some extensions
    env_line = f"\nOPENAI_API_BASE={nb_url}/v1\nOPENAI_API_KEY={api_key}\n"
    if not dry_run:
        if env_path.exists():
            content = env_path.read_text()
            if "OPENAI_API_BASE" not in content:
                with open(env_path, "a") as f:
                    f.write(env_line)
        else:
            env_path.write_text(env_line)
    else:
        click.echo(f"[DRY-RUN] Would append to {env_path}")
    
    paths.append(env_path)
    return paths

