def _setup_skill_agent(slug: str, skill_path: str, env_var: str, extra_config_path: Optional[str], 
                       project_dir: Path, nb_url: str, api_key: str, dry_run: bool, force: bool) -> List[Path]:
    paths = []
    full_skill_path = project_dir / skill_path
    content = _skill_file_content(slug.replace("-", " ").title(), env_var)
