import os
import json
import yaml
from pathlib import Path
from dataclasses import dataclass, field
from typing import Optional, Callable, Dict, List, Any
import click

@dataclass
class AgentIntegration:
    name: str           # display name e.g. "Claude Code"
    slug: str           # CLI slug e.g. "claude-code"
    phase: int          # 1, 2, or 3
    config_paths: list[str]   # relative paths this agent writes
    env_vars: Dict[str, str] = field(default_factory=dict)  # env var name → description
    skill_path: Optional[str] = None # for Phase 2 skill-file agents
    setup_fn: Optional[Callable] = None # _setup_<slug>(project_dir, nb_url, api_key, global_dir, dry_run, force) -> list[Path]
    launch_cmd: Optional[str] = None # CLI command to launch this agent
    launch_env_vars: Dict[str, str] = field(default_factory=dict) # env var name -> value (can use {nb_url}, {api_key})

def _nb_url(config=None) -> str:
    """Reads server.host/server.port from loaded config, falls back to http://localhost:8000"""
    if config and config.server:
        host = config.server.host
        if host == "0.0.0.0":
            host = "localhost"
        return f"http://{host}:{config.server.port}"
    return "http://localhost:8000"

def _api_key() -> str:
    """Reads NB_API_KEY env var, falls back to 'nb-local'"""
    return os.environ.get("NB_API_KEY", "nb-local")

def _write_json(path: Path, data: Dict[str, Any], dry_run: bool, force: bool) -> Path:
    """Writes JSON with '_neuralbroker': 'auto-generated' field."""
    data["_neuralbroker"] = "auto-generated"
    if dry_run:
        click.echo(f"[DRY-RUN] Would write JSON to {path}")
        return path
    
    if path.exists() and not force:
        if not click.confirm(f"File {path} already exists. Overwrite?", default=False):
            return path
            
    path.parent.mkdir(parents=True, exist_ok=True)
    with open(path, "w", encoding="utf-8") as f:
        json.dump(data, f, indent=2)
    return path

def _write_text(path: Path, content: str, dry_run: bool, force: bool) -> Path:
    """Writes text with comment header."""
    header = "# Generated by NeuralBroker — edit manually or re-run: neuralbrok integrations setup\n"
    if path.suffix == ".md":
        header = "<!-- Generated by NeuralBroker — edit manually or re-run: neuralbrok integrations setup -->\n"
    
    full_content = header + content
    
    if dry_run:
        click.echo(f"[DRY-RUN] Would write to {path}")
        return path
        
    if path.exists() and not force:
        if not click.confirm(f"File {path} already exists. Overwrite?", default=False):
            return path
            
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(full_content, encoding="utf-8")
    return path

def _skill_file_content(agent_name: str, env_var: str) -> str:
    """Generates markdown skill file template."""
    return f"""# NeuralBroker Skill for {agent_name}

This skill configures {agent_name} to use NeuralBroker as its primary inference engine.

## Configuration
- **API URL:** http://localhost:8000
- **Model:** Any OpenAI-compatible model (e.g., gpt-4o, claude-3-5-sonnet)
- **API Key:** Linked to your `{env_var}` environment variable.

## Instructions
When this skill is active, routing is handled automatically by NeuralBroker:
- High VRAM availability: Routes to local Ollama.
- Low VRAM availability: Routes to configured cloud providers (Groq, Together, etc.).
"""

